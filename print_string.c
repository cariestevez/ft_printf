#include "ft_printf.h"

int	print_string(char *s)
{
	int	i;
	int	len;

	i = 0;
	len = 0;
	if (s == 0)
		len = write (1, "(null)", 6);
	while (s[i] != 0)
	{
		len += write (1, &s[i], 1);
		i++;
	}
	return (len);
}
