#include "ft_printf.h"

char	*toupper_printf(char *s)
{
	int	i;

	i = 0;
	while (s[i] != 0)
	{
		if (s[i] > 96 && s[i] < 123)
			s[i] -= 32;
		i++;
	}
	return (s);
}
